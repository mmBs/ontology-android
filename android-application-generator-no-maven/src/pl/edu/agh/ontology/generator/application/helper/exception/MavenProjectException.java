package pl.edu.agh.ontology.generator.application.helper.exception;

public class MavenProjectException extends Exception {

	public MavenProjectException() {
		super();
	}

	public MavenProjectException(String message, Throwable cause) {
		super(message, cause);
	}

	public MavenProjectException(String message) {
		super(message);
	}

	public MavenProjectException(Throwable cause) {
		super(cause);
	}

}
